from random import randint

szamok:list[int] = []
betuk:list[str] = ['A', 'B', 'C', 'D', 'E', 'F']
rszam:int = randint(2, 5)
szamok.append(rszam)

for i in range(6):
    szamok[0] * betuk[0]
    szamok[1] * betuk[1]
    szamok[2] * betuk[2]
    szamok[3] * betuk[3]
    szamok[4] * betuk[4]
    print(betuk, end=' ')
